a=int(input("Enter the Number:"))
b=int(input("Enter the Number:"))
process=input("add or sub or mul or div:" )
if process=="add":
    print(a+b)
elif process=="sub":
    print(a-b)
elif process=="mul":
    print(a*b)
elif process=="div":
    print(a/b)
else:
    print("Invalid Operation")
    
    
    
