def add():
    a=int(input("Enter the A Value:"))
    b=int(input("Enter the B Value:"))
    print("The added value of A and B:",(a+b))


def sub():
    c=int(input("Enter the C Value:"))
    d=int(input("Enter the D Value:"))
    print("The added value of C and D:",(c-d))


def mul():
    e=int(input("Enter the E Value:"))
    f=int(input("Enter the F Value:"))
    print("The added value of E and F:",(e*f))


def div():
    g=int(input("Enter the G Value:"))
    h=int(input("Enter the H Value:"))
    print("The added value of G and H:",(g/h))


div()
add()
mul()
sub()

